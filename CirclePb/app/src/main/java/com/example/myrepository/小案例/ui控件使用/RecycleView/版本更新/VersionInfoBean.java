package com.example.myrepository.小案例.ui控件使用.RecycleView.版本更新;

public class VersionInfoBean {


    /**
     * version : 2
     * downloadApk : http://127.0.0.1:8080/version/test.apk
     * desc : 新年特别版
     */

    private String version;
    private String downloadApk;
    private String desc;

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public String getDownloadApk() {
        return downloadApk;
    }

    public void setDownloadApk(String downloadApk) {
        this.downloadApk = downloadApk;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
