package com.example.my_android.ui.main.EventBus;

public class MessageEvent {

    private String name;
    private int tag;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getTag() {
        return tag;
    }

    public void setTag(int tag) {
        this.tag = tag;
    }
}
